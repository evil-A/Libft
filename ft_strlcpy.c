#include "libft.h"

size_t	ft_strlcpy(char *dst, const char *src, size_t dstsize)
{
	size_t			i;
	size_t			size;
	unsigned char	*source;

	if (!dst)
		return (dstsize);
	size = ft_strlen(src);
	i = 0;
	source = (unsigned char *)src;
	if (dstsize > 0)
	{
		while (src[i] != '\0' && i < dstsize - 1)
		{
			dst[i] = src[i];
			i++;
		}
		dst[i] = '\0';
	}
	return (size);
}
