#include "libft.h"

char	*ft_strrchr(const char *s, int c)
{
	char	*in;
	int		i;

	i = 0;
	in = (char*)s;
	while (in[i] != '\0')
	{
		i++;
	}
	while (i >= 0)
	{
		if (in[i] == c)
			return (&in[i]);
		i--;
	}
	return (NULL);
}
